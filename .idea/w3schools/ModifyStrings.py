
# uppercase
a = "Hello World!"
print(a.upper())


# lowercase
print(a.lower())


# Remove Whitespace, strip() method removes any whitespace from the beginning or the end
txt = "   Hello, World!    "
print(txt.strip())


# Replace String
a = "Hello, World!"
print(a.replace("H", "J"))      # Jello, World! 


# Split String
print(a.split(","))     # ['Hello', ' World!']