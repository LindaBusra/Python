
# Join Two Tuples

# To join two or more tuples you can use the + operator:
tuple1 = ("a", "b" , "c")
tuple2 = (1, 2, 3)
tuple3 = tuple1 + tuple2
print(tuple3)


# Multiply Tuples
fruits = ("apple", "banana", "cherry")
mytuple = 2*fruits
print(mytuple)      # ('apple', 'banana', 'cherry', 'apple', 'banana', 'cherry')


list = (1, 2, 3)
newlist = 2*list
print(newlist)      # (1, 2, 3, 1, 2, 3)